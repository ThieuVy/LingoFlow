import React, { useState, useEffect } from 'react';
import { fetchStories, fetchFullStoryContent } from '../../services/gemini';
import { Story } from '../../types/types'; // Đảm bảo đúng path types
import ContentReader from '../reader/ContentReader';

interface EnglishStoriesProps {
  onReadStory?: (story: Story) => void; 
}

const EnglishStories: React.FC<EnglishStoriesProps> = () => {
  const [loading, setLoading] = useState(false);
  const [contentLoading, setContentLoading] = useState(false);
  const [genre, setGenre] = useState('Fairy Tales');
  const [stories, setStories] = useState<Story[]>([]);
  const [readingStory, setReadingStory] = useState<Story | null>(null);
  const [storyContent, setStoryContent] = useState<string>("");

  const loadStories = async () => {
    setLoading(true);
    try {
      const data = await fetchStories(genre);
      setStories(data);
    } catch (e) {
      console.error(e);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    loadStories();
  }, []);

  const handleStoryClick = async (story: Story) => {
      setContentLoading(true);
      setReadingStory(story);
      try {
          const fullText = await fetchFullStoryContent(story.title, story.author);
          setStoryContent(fullText);
      } catch (e) {
          console.error(e);
          setStoryContent("Failed to load story content.");
      } finally {
          setContentLoading(false);
      }
  };

  const genres = ['Fairy Tales', 'Sci-Fi', 'Mystery', 'Classic', 'Business', 'Horror', 'Romance'];

  if (readingStory) {
      return (
          <div className="relative">
             {contentLoading && (
                <div className="fixed inset-0 z-50 flex items-center justify-center bg-white/80 backdrop-blur-sm">
                    <div className="text-center">
                        <div className="w-16 h-16 border-4 border-indigo-600 border-t-transparent rounded-full animate-spin mx-auto mb-4"></div>
                        <h3 className="text-xl font-bold text-[#1B2559]">Writing Story...</h3>
                        <p className="text-[#A3AED0]">Creating the complete text for "{readingStory.title}"</p>
                    </div>
                </div>
            )}
            <ContentReader 
                initialContent={storyContent} 
                initialTitle={readingStory.title} 
                onBack={() => { setReadingStory(null); setStoryContent(""); }} 
            />
          </div>
      )
  }

  return (
    <div className="p-8 sm:p-12 max-w-7xl mx-auto">
      <div className="mb-10">
        <h2 className="text-4xl font-bold text-slate-900 tracking-tight font-serif">Short Stories</h2>
        <p className="text-slate-500 mt-2">Immersive reading experiences generated by AI (Full Text on Demand)</p>
      </div>

      <div className="flex overflow-x-auto pb-4 mb-8 space-x-3 custom-scrollbar">
        {genres.map(g => (
            <button
                key={g}
                onClick={() => { setGenre(g); setTimeout(loadStories, 0); }}
                className={`flex-shrink-0 px-6 py-3 rounded-xl font-bold text-sm transition-all ${
                    genre === g 
                    ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-200 scale-105' 
                    : 'bg-white text-slate-600 hover:bg-slate-50 border border-slate-200'
                }`}
            >
                {g}
            </button>
        ))}
      </div>

      {loading ? (
        <div className="grid grid-cols-1 md:grid-cols-3 gap-6 animate-pulse">
            {[1,2,3].map(i => (
                <div key={i} className="h-80 bg-slate-100 rounded-3xl"></div>
            ))}
        </div>
      ) : (
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
            {stories.map(story => (
                <div 
                    key={story.id || Math.random()}
                    onClick={() => handleStoryClick(story)}
                    className="bg-white rounded-3xl p-1 shadow-sm border border-slate-100 hover:shadow-xl hover:-translate-y-1 transition-all cursor-pointer group"
                >
                    <div className="bg-gradient-to-br from-slate-100 to-slate-200 h-40 rounded-t-[20px] rounded-b-xl mb-4 relative overflow-hidden">
                        <div className="absolute inset-0 flex items-center justify-center text-slate-300">
                             <svg className="w-16 h-16 opacity-50" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1} d="M12 6.253v13m0-13C10.832 5.477 9.246 5 7.5 5S4.168 5.477 3 6.253v13C4.168 18.477 5.754 18 7.5 18s3.332.477 4.5 1.253m0-13C13.168 5.477 14.754 5 16.5 5c1.747 0 3.332.477 4.5 1.253v13C19.832 18.477 18.247 18 16.5 18c-1.746 0-3.332.477-4.5 1.253" /></svg>
                        </div>
                        <div className="absolute top-4 left-4 bg-white/90 backdrop-blur px-3 py-1 rounded-lg text-xs font-bold text-slate-800 shadow-sm">
                            {story.level}
                        </div>
                    </div>
                    <div className="px-5 pb-6">
                        <h3 className="text-xl font-bold text-slate-800 mb-2 font-serif group-hover:text-indigo-600 transition-colors">{story.title}</h3>
                        <p className="text-xs font-bold text-slate-400 uppercase mb-3">By {story.author}</p>
                        <p className="text-slate-500 text-sm line-clamp-3 leading-relaxed">
                            {story.description}
                        </p>
                        <div className="mt-4 pt-3 border-t border-slate-50 flex items-center text-indigo-600 text-sm font-bold">
                            Read Full Story <svg className="w-4 h-4 ml-1" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 8l4 4m0 0l-4 4m4-4H3" /></svg>
                        </div>
                    </div>
                </div>
            ))}
        </div>
      )}
    </div>
  );
};

export default EnglishStories;